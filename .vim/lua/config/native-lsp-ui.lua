-- Native LSP UI configuration
-- Neovim 0.10+ built-in improvements

-- Enable inlay hints globally
if vim.lsp.inlay_hint then vim.lsp.inlay_hint.enable(true) end

-- Enhanced hover handler with rounded borders
vim.lsp.handlers["textDocument/hover"] = vim.lsp.with(vim.lsp.handlers.hover, {
  border = "rounded",
  max_width = 80,
  max_height = 20,
})

-- Enhanced signature help with rounded borders
vim.lsp.handlers["textDocument/signatureHelp"] = vim.lsp.with(vim.lsp.handlers.signature_help, {
  border = "rounded",
  max_width = 80,
})

-- Improved diagnostic display
vim.diagnostic.config {
  virtual_text = {
    spacing = 4,
    prefix = "‚óè",
  },
  signs = true,
  underline = true,
  update_in_insert = false,
  severity_sort = true,
  float = {
    border = "rounded",
    source = "always",
    header = "",
    prefix = "",
  },
}

-- Custom diagnostic signs
local signs = { Error = " ", Warn = " ", Hint = " ", Info = " " }
for type, icon in pairs(signs) do
  local hl = "DiagnosticSign" .. type
  vim.fn.sign_define(hl, { text = icon, texthl = hl, numhl = hl })
end
